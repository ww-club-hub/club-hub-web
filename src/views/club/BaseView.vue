import { type Club, type ClubRole } from "@/schema";
import { useRoute } from "vue-router";
import { doc } from "firebase/firestore";
import ClubTabLink from "@/components/ClubTabLink.vue";
import { getClaims, typedGetDoc } from "@/utils";
import { getIdToken } from "@firebase/auth";
import Loader from "@/components/Loader.vue";

const route = useRoute();

const clubId = route.params.clubId as string;

const claims = (await getClaims(auth))!;

const role: ClubRole = {
  stuco: claims.role == "owner" || claims.role == "admin",
  // officer permission
  officer: claims.officerOf?.[clubId] ?? 0,
  member: claims.memberOf?.includes(clubId) ?? false
};

const clubDoc = doc(db, "schools", claims.school, "clubs", clubId);

const club = await typedGetDoc<Club>(clubDoc);

async function checkAuthClaimDesync() {
  // TODO: 404 errors
  if (!club || !auth.currentUser) return;

  if (club.officers[auth.currentUser.email!]?.permissions !== role.officer) {
    // refresh token
    await getIdToken(auth.currentUser, true);
    location.reload();
  }
}

await checkAuthClaimDesync();

